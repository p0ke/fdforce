		$start=($last_block_of_file*$BLOCK_SIZE)+($SECTOR_SIZE*($i));
		$tmp=substr($FILE_CONTENT,$start,$SECTOR_SIZE); ///<<<<<< $SECTOR_SIZE au lieu de $SECTOR_SIZE-1
