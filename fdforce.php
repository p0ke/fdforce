function get_max_string_size($data)
{
	$SIZE_STRING=0;
	for($i=0;$i<count($data);$i++)
	{
		if(strlen($data[$i])>$SIZE_STRING)$SIZE_STRING=strlen($data[$i]);
	}
	return $SIZE_STRING;
}

function get_maxstr_size_multi($data,$nb_element_line)
{
	$SIZE_STRING=0;
	$COL_SIZE_LIST=array();
	$PADDING=1;
	for($j=0;$j<$nb_element_line;$j++)
	{
		for($i=$j;$i<count($data);$i+=$nb_element_line)
		{
			if(strlen(trim($data[$i]))>$SIZE_STRING)$SIZE_STRING=strlen(trim($data[$i]));
		}
		array_push($COL_SIZE_LIST,$SIZE_STRING+$PADDING);
		$SIZE_STRING=0;
	}
	return $COL_SIZE_LIST;
}

