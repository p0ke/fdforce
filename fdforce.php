function fat_block_chain($block_number)
{
	//echo "chain ok";
	//global $image;
	$fat='';
	$tmp='';
	//create 2 array one for block number and one for the number of sectors to be read.
	// default 8 sectors of 128 or 256 bytes per block
	//
	$chain_array=array();
	$sector_array=array();
	array_push($chain_array,$block_number);
	array_push($sector_array,8);
	// get the fat description on block 40 sector 2
	$fat.=get_sector(get_block(40),2);
	// block number has been pushed already (see above)
	// so we increase to get next block number in fat.
	$block_number++;
CHECKOTHERBLOCK:	
	//we base convert from binary to hexa (don't ask why !)
	$first_block=hexme($fat[$block_number]);
	//below 8F value is data so we push the block number increase and loop !!
	if(decme($first_block)<decme('8f')){
		array_push($chain_array,decme($first_block));
		array_push($sector_array,8);
		$block_number=decme($first_block); // block in fat are not automatically contiguous, the next block is the read block !
		$block_number=intval($block_number,$base=10)+1; // adjust read block position 
		
		GOTO CHECKOTHERBLOCK;
	}
	// value is sector used on last block
	if(decme($first_block)>=decme('c0') ){
		$first_block=decme($first_block)-decme('c0');
		// we remove last entry of sector and insert the new number of sector
		// we assume it is the last block of fat, so we combine both arrays to get keys and value
		// that we return
		array_pop($sector_array);
		array_push($sector_array,decme($first_block));
		return array_combine($chain_array, $sector_array);
	}
}

